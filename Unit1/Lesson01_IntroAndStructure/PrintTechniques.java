public class PrintTechniques {
    public static void main(String[] args) {
        // print() outputs on the same line
        System.out.print("Hello ");
        System.out.print("World");
        
        // println() outputs and moves to a new line
        System.out.println(" - Now with a newline!");
        
        // printf() allows formatted output
        int year = 2024;
        System.out.printf("Formatting demo: Year is %d", year);
    }
}
